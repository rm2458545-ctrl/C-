#include <stdio.h>
int main()
{ 
  char a = 'A';
  char b = 'B';
  printf("%d\n",a+b);

  int c = 10;
  unsigned int d = 20;
  printf("%u\n",c+d);

  float e  = 23.7F;
  float f = 11.5F;
  printf ("%lf\n",e+f);

  double g = 23.25;
  printf ("%lf\n",g);
  printf("%f\n",g);

  long h = 56666666666;
  unsigned long p  = 666666666;
  printf("%lld\n", h+p);

  return 0;
}

    
